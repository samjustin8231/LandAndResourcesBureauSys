		public List<Maticsoft.Model.T_Administrator> DataTableToList(DataTable dt)
		{
			List<Maticsoft.Model.T_Administrator> modelList = new List<Maticsoft.Model.T_Administrator>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Maticsoft.Model.T_Administrator model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

